function addTag(name, tag) {
  const escaped = name.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  const regex = new RegExp('(?<=, |: )' + escaped + '(?=,|$)', 'gm');
  data = data.replace(regex, name + '(' + tag + ')');
}

singers.forEach(n => addTag(n, '가수'));
actors.forEach(n => addTag(n, '배우'));
entertainers.forEach(n => addTag(n, '예능인'));

fs.writeFileSync('celeb-data.txt', data);

// 남은 태그 없는 사람 체크
const remaining = [];
data.split('\n').forEach(line => {
  const ci = line.indexOf(':');
  if (ci < 0) return;
  line.substring(ci + 1).trim().split(',').map(c => c.trim()).forEach(c => {
    if (c.length > 0 && !c.includes('(')) remaining.push(c);
  });
});
console.log('남은 태그없음: ' + remaining.length + '명');
if (remaining.length > 0) console.log(remaining.join('\n'));
